import fs from 'node:fs';
import path from 'node:path';

import { isExecutedAsScript, printViolations, repoRoot } from './shared.mjs';
import {
  collectCoverageOwnerInventory,
  synchronizeCoverageOwnerInventory,
} from './technical-debt-coverage.mjs';
import { synchronizePolicyDispositionInventory } from './technical-debt-policy-dispositions.mjs';
import {
  collectTechnicalDebtRegistryViolations,
  TECHNICAL_DEBT_REGISTRY_PATH,
} from './technical-debt-registry.mjs';

export const TECHNICAL_DEBT_REPORT_PATH = 'docs/engineering/tech-debt-report.md';

function readRegistry(root) {
  return JSON.parse(fs.readFileSync(path.join(root, TECHNICAL_DEBT_REGISTRY_PATH), 'utf8'));
}

function summarize(entries, key) {
  const counts = new Map();
  for (const entry of entries) {
    const value = key(entry);
    counts.set(value, (counts.get(value) ?? 0) + 1);
  }
  return [...counts]
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([value, count]) => `${value}=${count}`)
    .join(', ');
}

function compactEntry(entry) {
  return [
    `- \`${entry.id}\` — ${entry.owner}: ${entry.risk}`,
    `Action: ${entry.targetAction}`,
    `Review: ${entry.reviewBy}.`,
  ].join(' ');
}

function duplicationLine(entry) {
  return [
    `  - \`${entry.scope.family}\` — ${entry.scope.lines} lines`,
    `in ${entry.scope.count} block(s); ${entry.targetAction}.`,
  ].join(' ');
}

function coverageGapLine(entry) {
  return [
    `- \`${entry.owner}\` — ${entry.unownedFocusedFileCount} focused rollout file(s) lack an owner;`,
    `${entry.rolloutFileCount}/${entry.fileCount} production files are in rollout.`,
  ].join(' ');
}

function duplicationSection(entries) {
  const topDebt = entries
    .filter((entry) => entry.classification === 'debt')
    .sort((left, right) => right.scope.lines - left.scope.lines || left.id.localeCompare(right.id))
    .slice(0, 8);
  return [
    '## Duplication',
    '',
    [
      `- ${summarize(entries, (entry) => entry.classification)}.`,
      'Every family is exact-count, exact-lines, and sample-fingerprinted.',
    ].join(' '),
    '- Highest-line actionable families:',
    ...topDebt.map(duplicationLine),
  ];
}

function coverageSection(coverage, registryCoverage) {
  return [
    '## Focused coverage rollout',
    '',
    `- Owner disposition: ${summarize(coverage, (entry) => entry.observedDisposition)}.`,
    `- Registry disposition: ${summarize(registryCoverage, (entry) => entry.disposition)}.`,
    '- Owners with unowned focused rollout files:',
    ...coverage.filter((entry) => entry.unownedFocusedFileCount > 0).map(coverageGapLine),
  ];
}

export function generateTechnicalDebtReport({ root = repoRoot, registry = null } = {}) {
  const loaded = registry ?? readRegistry(root);
  const coverage = collectCoverageOwnerInventory({ root });
  const priorityEntries = loaded.entries.filter(
    (entry) => entry.classification === 'debt' && entry.source.kind !== 'jscpd'
  );
  const duplicationEntries = loaded.entries.filter((entry) => entry.source.kind === 'jscpd');
  const lines = [
    '# Technical debt',
    '',
    [
      'This report is generated from `tooling/configs/qa/technical-debt.data.json`.',
      'Change the enforced source and registry together; do not edit this file directly.',
    ].join(' '),
    '',
    '## Registry summary',
    '',
    `- Classification: ${summarize(loaded.entries, (entry) => entry.classification)}.`,
    `- Enforced source: ${summarize(loaded.entries, (entry) => entry.source.kind)}.`,
    '',
    '## Active structural debt',
    '',
    ...priorityEntries.map(compactEntry),
    '',
    ...duplicationSection(duplicationEntries),
    '',
    ...coverageSection(coverage, loaded.coverageOwners),
    '',
    '## Registered policy dispositions',
    '',
    `- ${summarize(loaded.policyDispositions, (entry) => entry.classification)}.`,
    '- Exact scopes, source hashes, owners, reasons, remediation, and expiry data remain in the machine registry.',
    '',
  ];
  return lines.join('\n');
}

function staleReportViolation() {
  return {
    rule: 'technical-debt-report-stale',
    file: TECHNICAL_DEBT_REPORT_PATH,
    message: 'Regenerate the report from the validated debt registry.',
  };
}

export function verifyTechnicalDebtReport({ root = repoRoot } = {}) {
  const violations = collectTechnicalDebtRegistryViolations({ root });
  if (violations.length > 0) return violations;
  const expected = generateTechnicalDebtReport({ root });
  const reportPath = path.join(root, TECHNICAL_DEBT_REPORT_PATH);
  const actual = fs.existsSync(reportPath) ? fs.readFileSync(reportPath, 'utf8') : '';
  return actual === expected ? [] : [staleReportViolation()];
}

export function writeTechnicalDebtReport({ root = repoRoot } = {}) {
  const violations = collectTechnicalDebtRegistryViolations({ root });
  if (violations.length > 0) return violations;
  fs.writeFileSync(
    path.join(root, TECHNICAL_DEBT_REPORT_PATH),
    generateTechnicalDebtReport({ root })
  );
  return [];
}

export function writePolicyDispositionInventory({ root = repoRoot } = {}) {
  const registryPath = path.join(root, TECHNICAL_DEBT_REGISTRY_PATH);
  const loaded = readRegistry(root);
  loaded.policyDispositions = synchronizePolicyDispositionInventory(loaded.policyDispositions, {
    root,
  });
  fs.writeFileSync(registryPath, `${JSON.stringify(loaded, null, 2)}\n`);
  return writeTechnicalDebtReport({ root });
}

export function writeCoverageOwnerInventory({ root = repoRoot } = {}) {
  const registryPath = path.join(root, TECHNICAL_DEBT_REGISTRY_PATH);
  const loaded = readRegistry(root);
  loaded.coverageOwners = synchronizeCoverageOwnerInventory(loaded.coverageOwners, { root });
  fs.writeFileSync(registryPath, `${JSON.stringify(loaded, null, 2)}\n`);
  return [];
}

if (isExecutedAsScript(import.meta.url)) {
  const write = process.argv.includes('--write');
  const writePolicy = process.argv.includes('--write-policy-dispositions');
  const writeCoverage = process.argv.includes('--write-coverage-owners');
  const violations = writeCoverage
    ? writeCoverageOwnerInventory()
    : writePolicy
      ? writePolicyDispositionInventory()
      : write
        ? writeTechnicalDebtReport()
        : verifyTechnicalDebtReport();
  if (violations.length > 0) {
    printViolations('Technical debt registry violations:', violations);
    process.exit(1);
  }
  process.stdout.write(
    writeCoverage
      ? `Coverage owner inventories generated: ${TECHNICAL_DEBT_REGISTRY_PATH}\n`
      : writePolicy
        ? `Policy dispositions and report generated: ${TECHNICAL_DEBT_REGISTRY_PATH}\n`
        : write
          ? `Technical debt report generated: ${TECHNICAL_DEBT_REPORT_PATH}\n`
          : 'Technical debt registry passed\n'
  );
}
